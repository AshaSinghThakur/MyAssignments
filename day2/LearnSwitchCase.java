package week1.day2;

public class LearnSwitchCase {
	public static void main(String[] args) {
		String Browser="edge";
		switch(Browser)
		{
		case "chrome":System.out.println("Google"); break;
		case "firefox":System.out.println("Mozilla"); break;
		case "edge":System.out.println("Microsoft"); break;

		}

	}	
	
	
}
